SELECT
	DISTINCT
	[FICHAJE].[LEGAJO] AS [Legajo]
	, CONVERT(VARCHAR, [FICHAJE].FCHREG, 112) [Fecha]
	, DATEPART(HOUR, [FICHAJE].HORAREG) [Hora]
	, DATEPART(MINUTE, [FICHAJE].HORAREG) [Minutos]
	--ESQUEMA
	, ASIGESQUEMA.ESQLAB 
	, ASIGESQUEMA.AREA 
	, ESQUEMA.Descr 
	, DETESQUEMA.HORAING 
	, [FICHAJE].HORAREG 
	, ASIGESQUEMA.NUMERO
	, ASIGESQUEMA.FMODIFW
	--*
FROM 
	ZL.ZNFICHAJE AS [FICHAJE]
	LEFT JOIN ZL.AsigEsqJLab AS ASIGESQUEMA ON ASIGESQUEMA.LEG = [FICHAJE].LEGAJO
		AND ASIGESQUEMA.FecHasta <> CONVERT(DATETIME, 0)
	LEFT JOIN ZL.SalRgDH AS ESQUEMA ON ESQUEMA.Codin = ASIGESQUEMA.ESQLAB 
	LEFT JOIN ZL.DtSalRgDH AS DETESQUEMA ON DETESQUEMA.Codint = ESQUEMA.Codin 
		AND DATEPART(dw, GETDATE()) = DETESQUEMA.Dia
WHERE
	/*LEGAJO = '0102'
	AND*/ CONVERT(VARCHAR, FCHREG, 112) = CONVERT(VARCHAR, GETDATE(), 112)
ORDER BY
	[FICHAJE].HORAREG 
	, [FICHAJE].LEGAJO 
	
GO
