CREATE PROCEDURE [Objetivos].[sp_CalculodeFiltrosxLegajo]
	@FECHA DATETIME = GETDATE
	, @LEGAJO VARCHAR(MAX)
AS
BEGIN
	SET NOCOUNT ON;
	DECLARE 
		@ID AS INT
		, @tmpValor AS NUMERIC(10,5);

	DECLARE @DETALLE TABLE(
		ID INT IDENTITY PRIMARY KEY
		, CODFIL INT
		, Filtro VARCHAR(254)
		, FiltroValor NUMERIC(10,5)
		, EscFiltroDesde NUMERIC(10,5)
		, EscFiltroHasta NUMERIC(10,5)
		, EscFiltroValor NUMERIC(10,5)
		, EscObjxPtoDesde NUMERIC(10,5)
		, EscObjxPtoHasta NUMERIC(10,5)
		, EscObjxPtoValor NUMERIC(10,5)
		, ObjeValorInicial NUMERIC(10,5)
		, PesoRelFiltro NUMERIC(10,5)
		, PesoRelEscala NUMERIC(10,5));

	IF @LEGAJO <> ''
	BEGIN
		INSERT INTO @DETALLE
			--SELECT 
			--	DISTINCT
			--	LEGAJOS.CCOD
			--	, LEGAJOS.CLEGAJO
			--	, FILTDET.CODFILT
			--	, FILTDET.DESCFILT
			--	, CONVERT(NVARCHAR(MAX), FILTROS.OBSQL)
			--	, ZL.ZL.OBJEINDIVALOBF.VALOR
			--	, ISNULL(ESCFILTRO.DESDE, 0)
			--	, ISNULL(ESCFILTRO.HASTA, 0)
			--	, ISNULL(ESCFILTRO.VALOR, 0)
			--	, 0.00
			--	, OBJINDIVIDUALES.CODIGO
			--	, ESCOBXPTO.DESDE
			--	, ESCOBXPTO.HASTA
			--	, ESCOBXPTO.VALOR
			--FROM 
			--	ZL.ZL.LEGOPS AS LEGAJOS
			--	INNER JOIN ZL.ZL.OBJEINDIVAL ON ZL.ZL.OBJEINDIVAL.LEGAJO = LEGAJOS.CLEGAJO
			--	LEFT JOIN ZL.ZL.CARRZL AS CARRERA ON CARRERA.CCOD = LEGAJOS.CLEGAJO
			--		AND ISNULL(CARRERA.CPUESTO, '''') <> ''''
			--	LEFT JOIN ZL.ZL.PUESTOSRH AS PUESTO ON PUESTO.COD = CARRERA.CPUESTO
			--	LEFT JOIN ZL.ZL.AREA AS AREAS ON AREAS.CODIGO = PUESTO.AREA
			--	LEFT JOIN ZL.ZL.SECTORES AS SECTOR ON SECTOR.CODIGO = AREAS.SECTOR  

			--	--OBJETIVOS POR PUESTO
			--	LEFT JOIN ZL.ZL.OBJEXPTO AS OBJINDIVIDUALES ON OBJINDIVIDUALES.PUESTO = PUESTO.COD
			--		AND @FECHA BETWEEN OBJINDIVIDUALES.DESDE AND OBJINDIVIDUALES.HASTA
			--	LEFT JOIN ZL.ZL.OBJEESVADE AS ESCOBXPTO ON ESCOBXPTO.CODIGO = OBJINDIVIDUALES.CODIGO

			--	--FILTROS POR PUESTO
			--	LEFT JOIN ZL.ZL.OBJEINDIVALOBF ON ZL.ZL.OBJEINDIVALOBF.CODIGO = ZL.ZL.OBJEINDIVAL.CODIGO
			--	LEFT JOIN ZL.ZL.FILTDET AS FILTDET ON FILTDET.CODIGO = ZL.ZL.OBJEINDIVALOBF.CODFIL
			--	LEFT JOIN ZL.ZL.FILTOBJE AS FILTROS ON FILTROS.PUESTO = FILTDET.CODFILT
			--	LEFT JOIN ZL.ZL.FILTOBJEDET AS ESCFILTRO ON ESCFILTRO.NUMERO = FILTDET.CODFILT
			--WHERE
