				, ZL.OBJEINDIVALOBF.VALOR [FiltroValor]
				, ZL.ZL.FILTOBJEDET.DESDE [EscFiltroDesde]
				, ZL.ZL.FILTOBJEDET.HASTA [EscFiltroHasta]
				, ZL.ZL.FILTOBJEDET.VALOR [EscFiltroValor]
				, ZL.OBJEESVADE.DESDE [EscObjxPtoDesde]
				, ZL.OBJEESVADE.HASTA [EscObjxPtoHasta]
				, ZL.OBJEESVADE.VALOR [EscObjxPtoValor]
				, ZL.OBJEINDIVALOBD.VALOR [ObjeValorInicial]
				, 0.00
				, 0.00
