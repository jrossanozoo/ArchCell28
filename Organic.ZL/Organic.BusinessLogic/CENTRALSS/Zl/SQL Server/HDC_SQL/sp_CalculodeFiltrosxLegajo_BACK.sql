			WHERE
				ZL.CARRZL.CCOD = @LEGAJO
				AND @FECHA BETWEEN ZL.CARRZL.FINICIO AND CASE ZL.CARRZL.FFIN WHEN 0 THEN GETDATE() ELSE ZL.CARRZL.FFIN END
				
	END
