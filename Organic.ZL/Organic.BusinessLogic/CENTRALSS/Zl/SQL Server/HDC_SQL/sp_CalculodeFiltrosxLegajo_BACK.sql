				--CON EL LEGAJO TENEMOS LOS OBJETIVOS Y FILTROS CALCULADOS INDIVIDUALMENTE
				LEFT JOIN ZL.OBJEINDIVAL ON ZL.OBJEINDIVAL.LEGAJO = ZL.CARRZL.CCOD
				--LEVANTAMOS CADA FILTRO INDIVIDUAL	
				LEFT JOIN ZL.OBJEINDIVALOBF ON ZL.OBJEINDIVALOBF.CODIGO = ZL.OBJEINDIVAL.CODIGO
				--LEVANTAMOS LA ESCALA PARA CADA FILTRO INDIVIDUAL
				LEFT JOIN ZL.FILTOBJE ON ZL.FILTOBJE.COD = ZL.OBJEINDIVALOBF.CODFIL
				LEFT JOIN ZL.FILTOBJEDET ON ZL.FILTOBJEDET.NUMERO = ZL.FILTOBJE.COD
					AND ZL.FILTOBJEDET.HASTA <> 0
				--LEVANTAMOS LA ESCALA DEL OBJETIVO POR PUESTO PARA EL RESULTADO FINAL
				LEFT JOIN ZL.OBJEXPTO ON ZL.OBJEXPTO.PUESTO = ZL.CARRZL.CPUESTO
				LEFT JOIN ZL.OBJEESVADE ON ZL.OBJEESVADE.CODIGO = ZL.OBJEXPTO.CODIGO
					AND ZL.OBJEESVADE.HASTA <> 0
				--LEVANTAMOS LOS OBJETIVOS INDIVIDUALES PARA SABER SU VALOR INICIAL
				LEFT JOIN ZL.OBJEINDIVALOBD ON ZL.OBJEINDIVALOBD.CODIGO = ZL.OBJEINDIVAL.CODIGO
	END
	--CREAMOS UN CURSOR PARA RECORRER Y AGREGAR Y MODIFICAR DATOS COMO PESO Y RESULTADO DE LOS OBJETOS SQL
	DECLARE DETALLE CURSOR FOR SELECT ID FROM @DETALLE;
	OPEN DETALLE;

	FETCH NEXT FROM DETALLE INTO @ID;
	WHILE @@FETCH_STATUS = 0
	BEGIN
		--SELECT @tmpValor = EscFiltroValor FROM @DETALLE WHERE ObjeValorInicial >= EscFiltroDesde AND ObjeValorInicial <= EscFiltroHasta AND ID = @ID
		--UPDATE @DETALLE SET EscFiltroValor = ISNULL(@tmpValor, 0) WHERE ID = @ID

		SELECT @tmpValor = (SELECT EscFiltroValor * ObjeValorInicial FROM @DETALLE WHERE ID = @ID AND ObjeValorInicial >= EscFiltroDesde AND ObjeValorInicial <= EscFiltroHasta)
		UPDATE @DETALLE SET PesoRelFiltro = ISNULL(@tmpValor, 0) WHERE ID = @ID

		SET @tmpValor = 0;
		SET @ID = NULL;
		FETCH NEXT FROM DETALLE INTO @ID;
	END

	SELECT
		DISTINCT
		* 
	FROM 
		@DETALLE
	--WHERE 
	--	ObjexPuestoCod IS NOT NULL
