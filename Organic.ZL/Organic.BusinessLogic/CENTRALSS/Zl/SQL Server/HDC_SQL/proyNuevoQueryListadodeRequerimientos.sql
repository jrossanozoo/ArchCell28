		ELSE CASE 
			WHEN (REQUERIMIENTOS.FUNCIONALIDAD = 0 AND REQUERIMIENTOS.BUG = 0) THEN REQUERIMIENTOS.ESTADOPREANALISIS
			ELSE CASE 
				WHEN REQUERIMIENTOS.BUG <> 0 THEN ISNULL(BUE.ESTADO,' SIN ESTADO ') 
				ELSE ISNULL(FUE.ESTADO,' SIN ESTADO ') 
			END 
		END 
