CREATE FUNCTION [OBJETIVOS].[GradoDeServicio] 
(	@FECHAINICIO AS DATETIME,
	@FECHAFIN AS DATETIME )
  
  RETURNS @GradoDeServicio TABLE (
		VALOR NUMERIC(10,4)
	)
AS
BEGIN
	INSERT INTO @GradoDeServicio	
	SELECT
		ROUND((CONVERT(NUMERIC(10,2), SUM([LLAMADAS ATENDIDAS EN 60]))/(SUM([LLAMADAS ATENDIDAS])+SUM([LLAMADAS PERDIDAS]))), 4) VALOR
	FROM
		TECNOVOZ.DBO.VISTAACD
	WHERE
		[LLAMADAS EXTERNAS] = 1
		AND [AčO] BETWEEN YEAR(@FechaInicio) AND YEAR(@FechaFin)
		AND [MES] BETWEEN MONTH (@FechaInicio) AND MONTH (@FechaFin)

RETURN

END


