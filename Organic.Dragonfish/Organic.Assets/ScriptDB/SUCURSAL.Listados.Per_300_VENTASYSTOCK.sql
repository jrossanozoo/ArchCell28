	cast( @Parametro2 as varchar( 60 ) ) as FACTURA_VentasDesdeVirtual,
	cast( @Parametro3 as varchar( 60 ) ) as FACTURA_PeriodoConsideradoVirtual,
	cast( funciones.obtenerremitossinfacturardesalidadelacombinacion(c_STOCKCOMBINACION.COART, c_STOCKCOMBINACION.COCOL, c_STOCKCOMBINACION.talle, @parametro3) as numeric( 15, 2 ) ) as ITEMARTICULOSVENTAS_RemitosSinFacturarVirtual,
