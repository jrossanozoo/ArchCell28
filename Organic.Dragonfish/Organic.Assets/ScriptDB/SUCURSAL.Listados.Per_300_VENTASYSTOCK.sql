	ZooLogic.COMB as c_STOCKCOMBINACION 
	left join (
		select ITEMARTICULOSVENTAS_FART,
				ITEMARTICULOSVENTAS_CCOLOR,
				ITEMARTICULOSVENTAS_TALLE
			from [listados].[PER_300_ITEMARTICULOSVENTAS] (  @BaseDeDatos, @ARTICULO_PROVEEDOR_DESDE, @ARTICULO_PROVEEDOR_HASTA, @PROVEEDOR_NOMBRE_DESDE, @PROVEEDOR_NOMBRE_HASTA, @ARTICULO_UNIDADDEMEDIDA_DESDE, @ARTICULO_UNIDADDEMEDIDA_HASTA, @ARTICULO_TEMPORADA_DESDE, @ARTICULO_TEMPORADA_HASTA, @ARTICULO_ANO_DESDE, @ARTICULO_ANO_HASTA, @ARTICULO_FAMILIA_DESDE, @ARTICULO_FAMILIA_HASTA, @ARTICULO_MATERIAL_DESDE, @ARTICULO_MATERIAL_HASTA, @ARTICULO_LINEA_DESDE, @ARTICULO_LINEA_HASTA, @ARTICULO_GRUPO_DESDE, @ARTICULO_GRUPO_HASTA, @ARTICULO_CATEGORIADEARTICULO_DESDE, @ARTICULO_CATEGORIADEARTICULO_HASTA, @ARTICULO_CLASIFICACION_DESDE, @ARTICULO_CLASIFICACION_HASTA, @ARTICULO_TIPODEARTICULO_DESDE, @ARTICULO_TIPODEARTICULO_HASTA, @ARTICULO_NOCOMERCIALIZABLE_DESDE, @ARTICULO_NOCOMERCIALIZABLE_HASTA, @ARTICULO_DESCRIPCIONADICIONAL_DESDE,	@ARTICULO_DESCRIPCIONADICIONAL_HASTA, @FACTURA_FECHA_DESDE, @FACTURA_FECHA_HASTA, @ARTICULO_COMPORTAMIENTO, @UNIDADDEMEDIDA_UNIDADDEMEDIDA_DESCRIPCION_DESDE, @UNIDADDEMEDIDA_UNIDADDEMEDIDA_DESCRIPCION_HASTA, @TEMPORADA_TEMPORADA_DESCRIPCION_DESDE, @TEMPORADA_TEMPORADA_DESCRIPCION_HASTA, @FAMILIA_FAMILIA_DESCRIPCION_DESDE, @FAMILIA_FAMILIA_DESCRIPCION_HASTA, @MATERIAL_MATERIAL_DESCRIPCION_DESDE, @MATERIAL_MATERIAL_DESCRIPCION_HASTA, @LINEA_LINEA_DESCRIPCION_DESDE, @LINEA_LINEA_DESCRIPCION_HASTA, @GRUPO_GRUPO_DESCRIPCION_DESDE, @GRUPO_GRUPO_DESCRIPCION_HASTA, @CATEGORIADEARTICULO_CATEGORIADEARTICULO_DESCRIPCION_DESDE, @CATEGORIADEARTICULO_CATEGORIADEARTICULO_DESCRIPCION_HASTA, @CLASIFICACIONARTICULO_CLASIFICACION_DESCRIPCION_DESDE, @CLASIFICACIONARTICULO_CLASIFICACION_DESCRIPCION_HASTA, @TIPODEARTICULO_TIPODEARTICULO_DESCRIPCION_DESDE, @TIPODEARTICULO_TIPODEARTICULO_DESCRIPCION_HASTA, @ITEMARTICULOSVENTAS_ARTICULO_DESDE, @ITEMARTICULOSVENTAS_ARTICULO_HASTA, @ITEMARTICULOSVENTAS_ARTICULODETALLE_DESDE, @ITEMARTICULOSVENTAS_ARTICULODETALLE_HASTA, @ITEMARTICULOSVENTAS_COLOR_DESDE, @ITEMARTICULOSVENTAS_COLOR_HASTA, @ITEMARTICULOSVENTAS_COLORDETALLE_DESDE, @ITEMARTICULOSVENTAS_COLORDETALLE_HASTA, @ITEMARTICULOSVENTAS_TALLE_DESDE, @ITEMARTICULOSVENTAS_TALLE_HASTA, @ITEMARTICULOSVENTAS_TALLEDETALLE_DESDE, @ITEMARTICULOSVENTAS_TALLEDETALLE_HASTA, @Parametro1, @Parametro2, @Parametro3, @Parametro4 ) 
				) as C_VENTASYSTOCK on
									c_STOCKCOMBINACION.COART = C_VENTASYSTOCK.ITEMARTICULOSVENTAS_FART and
									c_STOCKCOMBINACION.COCOL = C_VENTASYSTOCK.ITEMARTICULOSVENTAS_CCOLOR and
									c_STOCKCOMBINACION.TALLE = C_VENTASYSTOCK.ITEMARTICULOSVENTAS_TALLE

	 left join ZooLogic.ART as C_ARTICULO on C_ARTICULO.ARTCOD = c_STOCKCOMBINACION.COART and 0 = Funciones.empty( C_ARTICULO.ARTCOD )
	 left join ZooLogic.CATEGART as C_CATEGORIADEARTICULO on C_CATEGORIADEARTICULO.COD = C_ARTICULO.CATEARTI and 0 = Funciones.empty( C_CATEGORIADEARTICULO.COD )
	 left join ZooLogic.CLASIFART as C_CLASIFICACIONARTICULO on C_CLASIFICACIONARTICULO.CODIGO = C_ARTICULO.CLASIFART and 0 = Funciones.empty( C_CLASIFICACIONARTICULO.CODIGO )
