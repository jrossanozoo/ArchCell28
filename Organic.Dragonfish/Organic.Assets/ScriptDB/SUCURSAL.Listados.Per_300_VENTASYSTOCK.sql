MARTICULOSVENTAS_COLORDETALLE_HASTA, @ITEMARTICULOSVENTAS_TALLE_DESDE, @ITEMARTICULOSVENTAS_TALLE_HASTA, @ITEMARTICULOSVENTAS_TALLEDETALLE_DESDE, @ITEMARTICULOSVENTAS_TALLEDETALLE_HASTA, @Parametro1, @Parametro2, @Parametro3, @Parametro4 ) 
	
