	case when C_CLASIFICACIONARTICULO.DESCRIP is null then '' else C_CLASIFICACIONARTICULO.DESCRIP end as CLASIFICACIONARTICULO_DESCRIP ,
	case when C_TIPODEARTICULO.DESCRIP is null then '' else C_TIPODEARTICULO.DESCRIP end as TIPODEARTICULO_DESCRIP ,
