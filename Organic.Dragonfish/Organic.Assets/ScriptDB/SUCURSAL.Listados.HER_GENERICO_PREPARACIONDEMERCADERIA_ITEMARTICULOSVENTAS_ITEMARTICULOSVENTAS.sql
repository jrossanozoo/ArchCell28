, ComprobantesAfectantes as
	( /* Excluye los Picking que no afectan cantidades */
	select 
		  dc.IDITEM
		, dc.FCANT
		, cast( coalesce( dp.CANTAFE, 0 ) as numeric( 9, 3 ) ) CPicking
		, Afecta.AFETIPOCOM
		, Afecta.AFECTA
		, Afecta.CODIGO
	from ( select CODIGO, AFECTA, CODINTER, AFETIPOCOM from ZooLogic.COMPAFE where AFETIPO = 'Afectante' ) Afecta
		inner join ( select CODIGO, IDITEM, AFE_COD, FCANT, FART, CCOLOR, TALLE from ZooLogic.COMPROBANTEVDET ) dc on Afecta.CODIGO = dc.AFE_COD and Afecta.AFECTA = dc.CODIGO
		left join ( select CODIGO, CODIGOORI, IDITEM, (select sum(cantafe) from zoologic.DETPREPMPICKING as b where a.IDITEM = b.IDITEM and a.CODIGOORI = b.CODIGOORI ) as cantafe from ZooLogic.DETPREPMPICKING as a ) dp on Afecta.CODIGO = dp.CODIGOORI and dc.IDITEM = dp.IDITEM
		left join ( select CODIGO, NROITEM, CPCANTIDAD, CPARTICULO, CPCOLOR, CPTALLE from ZooLogic.DETCONPICKING ) dp2 on Afecta.CODINTER = dp2.CODIGO and dc.FART = dp2.CPARTICULO and dc.CCOLOR = dp2.CPCOLOR and dc.TALLE = dp2.CPTALLE
	where 1=1
		and coalesce( dp.CANTAFE, dp2.CPCANTIDAD, power( 0, sign( len( ltrim( rtrim( Afecta.CODINTER ) ) ) ) ) ) > 0
	group by dc.IDITEM, dc.FCANT, cast( coalesce( dp.CANTAFE, 0 ) as numeric( 9, 3 ) ), Afecta.AFETIPOCOM, Afecta.AFECTA, Afecta.CODIGO
	)
, ComprobantesRelacionadosAux( IDITEM, CODIGO, Cantidad, CPicking, Tipo, TipoAfectado, Secuencia, SecuenciaAfectada, Afectado, Nivel ) as
	(
		select Principal.IDITEM,
			Principal.CODIGO,
			Principal.FCANT,
			cast( 0 as numeric(9,3)),
			Principal.FACTTIPO,
			cast( 0 as numeric(2,0)),
			cast( case 
				when Principal.FACTTIPO = 23 then 1	-- Pedido
				when Principal.FACTTIPO = 57 then 2	-- Preparacion de mercaderia
				when Principal.FACTTIPO = 11 then 3	-- Remito
				when Principal.FACTTIPO in (1, 2, 27, 33, 47) then 4	-- Facturas
				else 5
			end as numeric(2, 0)),
			cast( 0 as numeric(2,0)),
			Principal.CODIGO,
			0 as nivel
		from ( select CODIGO, IDITEM, FACTTIPO, FCANT from ConjuntoBase ) as Principal

		union all

		select Anterior.IDITEM,
			Anterior.CODIGO,
			Afecta.FCANT,
			cast( case when Afecta.AFETIPOCOM != 12 then Afecta.CPicking * power( 0, Anterior.Nivel ) else 0 end as numeric(9,3)),
			Afecta.AFETIPOCOM,
			Anterior.Tipo,
			cast(case 
				when Afecta.AFETIPOCOM = 23 then 1	-- Pedido
				when Afecta.AFETIPOCOM = 57 then 2	-- Preparacion de mercaderia
				when Afecta.AFETIPOCOM = 11 then 3	-- Remito
				when Afecta.AFETIPOCOM in (1, 2, 27, 33, 47) then 4	-- Facturas
				when Afecta.AFETIPOCOM in (12, 3, 5, 28, 35, 48) and Anterior.TipoAfectado != 12 then -case 
																					when Anterior.Tipo = 25 then 1
																					when Anterior.Tipo = 23 then 2
																					when Anterior.Tipo = 11 then 3
																					when Anterior.Tipo in (1, 2, 27, 33, 47) then 4
																				else 5
																			end
			else 5
			end as numeric(2, 0)),
			cast(case 
				when Anterior.Tipo = 25 then 1
				when Anterior.Tipo = 23 then 2
				when Anterior.Tipo = 11 then 3
				when Anterior.Tipo in (1, 2, 27, 33, 47) then 4
			else 5
			end as numeric(2, 0)),
			Afecta.AFECTA,
			Anterior.nivel + 1
		from ComprobantesRelacionadosAux as Anterior
			inner join ComprobantesAfectantes Afecta on Anterior.Afectado = Afecta.CODIGO and Anterior.IDITEM = Afecta.IDITEM
	where 1=1
			and Anterior.nivel < 5
	)
, CantidadesDistribuidas as
	(
		select cantidades.CODIGO, cantidades.IDITEM, cantidades.CPicking
			, case cantidades.Secuencia          when  1								then cantidades.cantidad else 0 end as ConfirmadoS1
			, case cantidades.Secuencia          when -1								then cantidades.cantidad else 0 end as CanceladoS1
			, case when cantidades.SecuenciaAfectada = 1 and cantidades.Secuencia > 0	then cantidades.cantidad else 0 end as AfectadoS1
			, case cantidades.Secuencia          when  2								then cantidades.cantidad else 0 end as ConfirmadoS2
			, case cantidades.Secuencia          when -2								then cantidades.cantidad else 0 end as CanceladoS2
			, case when cantidades.SecuenciaAfectada = 2 and cantidades.Secuencia > 0	then cantidades.cantidad else 0 end as AfectadoS2
			, case cantidades.Secuencia          when  3								then cantidades.cantidad else 0 end as ConfirmadoS3
			, case cantidades.Secuencia          when -3								then cantidades.cantidad else 0 end as CanceladoS3
			, case when cantidades.SecuenciaAfectada = 3 and cantidades.Secuencia > 0	then cantidades.cantidad else 0 end as AfectadoS3
			, case cantidades.Secuencia          when  4								then cantidades.cantidad else 0 end as ConfirmadoS4
			, case cantidades.Secuencia          when -4								then cantidades.cantidad else 0 end as CanceladoS4
			, case when cantidades.SecuenciaAfectada = 4 and cantidades.Secuencia > 0	then cantidades.cantidad else 0 end as AfectadoS4
