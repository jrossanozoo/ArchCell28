	( ( @REGISTRODECUENTA_FECHA_DESDE is null ) OR ( c_REGISTRODECUENTA.Fecha >= @REGISTRODECUENTA_FECHA_DESDE ) ) and 
	( ( @REGISTRODECUENTA_FECHA_HASTA is null ) OR ( c_REGISTRODECUENTA.Fecha <= @REGISTRODECUENTA_FECHA_HASTA ) ) and 
	( ( @REGISTRODECUENTA_FECHACARGAOPERACION_DESDE is null ) OR ( c_REGISTRODECUENTA.FechaCarga >= @REGISTRODECUENTA_FECHACARGAOPERACION_DESDE ) ) and 
	( ( @REGISTRODECUENTA_FECHACARGAOPERACION_HASTA is null ) OR ( c_REGISTRODECUENTA.FechaCarga <= @REGISTRODECUENTA_FECHACARGAOPERACION_HASTA ) ) and 
	( ( @REGISTRODECUENTA_HORA_DESDE is null ) OR ( c_REGISTRODECUENTA.Hora >= @REGISTRODECUENTA_HORA_DESDE ) ) and 
