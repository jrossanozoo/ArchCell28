	( ( @REGISTRODECUENTA_TIPOVALOR is null ) OR ( c_REGISTRODECUENTA.TipoVal = @REGISTRODECUENTA_TIPOVALOR ) ) and 
	( ( @REGISTRODECUENTA_IDENTIFICADOR_DESDE is null ) OR ( c_REGISTRODECUENTA.Ident >= @REGISTRODECUENTA_IDENTIFICADOR_DESDE ) ) and 
	( ( @REGISTRODECUENTA_IDENTIFICADOR_HASTA is null ) OR ( c_REGISTRODECUENTA.Ident <= @REGISTRODECUENTA_IDENTIFICADOR_HASTA ) ) and 
