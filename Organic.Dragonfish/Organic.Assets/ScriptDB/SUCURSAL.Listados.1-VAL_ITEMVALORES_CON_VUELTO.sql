IF  EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[Listados].[VAL_ITEMVALORES_CON_VUELTO]') AND type in (N'FN', N'IF', N'TF', N'FS', N'FT'))
DROP FUNCTION [Listados].[VAL_ITEMVALORES_CON_VUELTO];
GO;

CREATE FUNCTION [Listados].[VAL_ITEMVALORES_CON_VUELTO]
(
	@ITEMVALORES_FECHA_DESDE datetime,
	@ITEMVALORES_FECHA_HASTA datetime
)
RETURNS TABLE
AS
RETURN
(
select 
	c_FACTURA.FALTAFW + convert( datetime, c_FACTURA.HALTAFW) as MARCATEMPORAL,
	1 as ORDEN,
	c_ITEMVALORES.COTIZA, 
	c_ITEMVALORES.CUPON, 
	c_ITEMVALORES.FECHCOTI, 
	c_ITEMVALORES.GUIDCUPON, 
	c_FACTURA.IDCAJA, 
	c_ITEMVALORES.JJCO, 
	c_ITEMVALORES.JJCOTIZ, 
	c_ITEMVALORES.JJDE, 
	c_ITEMVALORES.JJFE, 
	c_FACTURA.FFCH as JJFECHA, 
	c_ITEMVALORES.JJM, 
	c_ITEMVALORES.JJNUM, 
	c_FACTURA.FACTTIPO as JJT, 
	c_ITEMVALORES.JJTOTFAC, 
	c_ITEMVALORES.JJRECIB, 
	c_FACTURA.FTURNO as JJTURNO, 
	c_FACTURA.FVEN as JJVEN, 
	c_FACTURA.FLETRA as LETRA, 
	c_ITEMVALORES.NROCHEQUE, 
	c_ITEMVALORES.NROCHPROP, 
	c_ITEMVALORES.NRODOC,
	c_ITEMVALORES.NROINTERNO, 
	c_ITEMVALORES.NROITEM, 
	c_FACTURA.FNUMCOMP as NUMCOMPR, 
	c_ITEMVALORES.PERMVUELTO, 
	c_ITEMVALORES.PERSCOMP, 
	c_ITEMVALORES.PESOS, 
	c_ITEMVALORES.RECPESOS, 
	c_FACTURA.FPTOVEN as PTOVENTA, 
	c_ITEMVALORES.RECMONTO, 
	C_ITEMVALORES.MONTOSISTE,
	c_ITEMVALORES.RECPORC, 
	c_ITEMVALORES.RECSINIMP, 
	c_ITEMVALORES.RECSINPER, 
	c_ITEMVALORES.REDONDEO, 
	cast( sign( c_ITEMVALORES.SIGNO + ( c_FACTURA.SIGNOMOV - 1 ) * c_FACTURA.SIGNOMOV ) as numeric(2, 0)) as SIGNO, 
	c_ITEMVALORES.TIPCOMP, 
	c_ITEMVALORES.TIPODOC, 
	c_ITEMVALORES.VALECAMBIO, 
	c_ITEMVALORES.AUTORIZADO, 
	c_ITEMVALORES.IDITEM, 
	c_ITEMVALORES.DESCMONTO, 
	c_ITEMVALORES.DESCPORC, 
	c_ITEMVALORES.DESCSINIMP, 
	c_ITEMVALORES.MONDESPES, 
	c_ITEMVALORES.MONDESREC, 
	c_ITEMVALORES.MONRECPES, 
	c_ITEMVALORES.PORCDESREC,
	c_ITEMVALORES.ESVUELTO,
	c_FACTURA.faltafw,
	c_FACTURA.HALTAFW,
	c_FACTURA.BDALTAFW,
	c_FACTURA.BDMODIFW,
	c_FACTURA.ESTTRANS,
	c_FACTURA.fecexpo,
	c_FACTURA.fecimpo, 
	c_FACTURA.fmodifw,
	c_FACTURA.fectrans,
	c_FACTURA.HORAEXPO,
	c_FACTURA.HORAIMPO,
	c_FACTURA.HMODIFW,
	c_FACTURA.SALTAFW, 
	c_FACTURA.SMODIFW, 
	c_FACTURA.UALTAFW, 
	c_FACTURA.UMODIFW, 
	c_FACTURA.VALTAFW, 
	c_FACTURA.VMODIFW, 
	c_FACTURA.ZADSFW 
from ZooLogic.VAL as c_ITEMVALORES
left join ZooLogic.COMPROBANTEV as c_FACTURA on c_FACTURA.CODIGO = c_ITEMVALORES.JJNUM
where ( C_FACTURA.ANULADO = 0 ) and
	( ( @ITEMVALORES_FECHA_DESDE is null ) OR ( C_FACTURA.FFCH >= @ITEMVALORES_FECHA_DESDE ) ) and 
	( ( @ITEMVALORES_FECHA_HASTA is null ) OR ( C_FACTURA.FFCH <= @ITEMVALORES_FECHA_HASTA ) )
union all
select 
	c_VUELTO.FALTAFW + convert( datetime, c_VUELTO.HALTAFW) as MARCATEMPORAL,
	2 as ORDEN,
	cast( 1 as numeric(16, 5)) as COTIZA, 
	cast (' ' as char(38)) as CUPON, 
	Convert(DateTime, '19000101') as FECHCOTI, 
	cast ('' as varchar(38)) as GUIDCUPON, 
	c_VUELTO.IDCAJA, 
	cast( c_VUELTO.IDVUELTO as char(5)) as JJCO, 
	cast( 1 as numeric(16, 5)) as JJCOTIZ, 
	cast( 'Vuelto comprobante' as varchar(30)) as JJDE, 
	c_VUELTO.FFCH as JJFE, 
	c_VUELTO.FFCH as JJFECHA, 
	cast( c_VUELTO.FVUELTO as numeric(13, 2)) as JJM, 
	c_VUELTO.CODIGO as JJNUM, 
	c_VUELTO.FACTTIPO as JJT, 
	cast( c_VUELTO.FVUELTO as numeric(13, 2)) as JJTOTFAC, 
	cast( c_VUELTO.FVUELTO as numeric(13, 2)) as JJRECIB,
	c_VUELTO.FTURNO as JJTURNO, 
	cast( c_VUELTO.FVEN as varchar(5)) as JJVEN, 
	c_VUELTO.FLETRA as LETRA, 
	cast( ' ' as char(38)) as NROCHEQUE, 
	cast( ' ' as char(38)) as NROCHPROP, 
	cast( '' as varchar(10)) as NRODOC,
	cast( '' as varchar(15)) as NROINTERNO, 
	cast( 0 as numeric(5, 0)) as NROITEM, 
	c_VUELTO.FNUMCOMP as NUMCOMPR, 
	cast( 0 as bit) as PERMVUELTO, 
	cast( 0 as bit) as PERSCOMP, 
	cast( c_VUELTO.FVUELTO as numeric(13, 2)) as PESOS, 
	cast( c_VUELTO.FVUELTO as numeric(13, 2)) as RECPESOS,
	c_VUELTO.FPTOVEN as PTOVENTA, 
	cast( 0 as numeric(12, 2)) as RECMONTO,
	cast( 0 as numeric(12, 2)) as MONTOSISTE,
	cast( 0 as numeric(6, 2)) as RECPORC,
	cast( 0 as numeric(12, 2)) as RECSINIMP,
	cast( 0 as numeric(12, 2)) as RECSINPER,
	cast( '' as varchar(10)) as REDONDEO,
	cast( power( 0, 1 + c_VUELTO.SIGNOMOV * -1 ) as numeric(2, 0)) as SIGNO, 
	cast( '' as varchar(3)) as TIPCOMP, 
	cast( '' as varchar(2)) as TIPODOC, 
	cast( ' ' as char(38)) as VALECAMBIO, 
	cast( 0 as bit) as AUTORIZADO, 
	cast( '' as varchar(38)) as IDITEM, 
	cast( 0 as numeric(12, 2)) as DESCMONTO,
	cast( 0 as numeric(6, 2)) as DESCPORC,
	cast( 0 as numeric(15, 2)) as DESCSINIMP,
	cast( 0 as numeric(12, 2)) as MONDESPES,
	cast( 0 as numeric(12, 2)) as MONDESREC,
	cast( 0 as numeric(12, 2)) as MONRECPES,
	cast( 0 as numeric(6, 2 )) as PORCDESREC,
	cast( 1 as bit) as EsVuelto,
	c_VUELTO.faltafw,
	c_VUELTO.HALTAFW,
	c_VUELTO.BDALTAFW,
	c_VUELTO.BDMODIFW,
	c_VUELTO.ESTTRANS,
	c_VUELTO.fecexpo,
	c_VUELTO.fecimpo, 
	c_VUELTO.fmodifw,
	c_VUELTO.fectrans,
	c_VUELTO.HORAEXPO,
	c_VUELTO.HORAIMPO,
	c_VUELTO.HMODIFW,
	c_VUELTO.SALTAFW, 
	c_VUELTO.SMODIFW, 
	c_VUELTO.UALTAFW, 
	c_VUELTO.UMODIFW, 
	c_VUELTO.VALTAFW, 
	c_VUELTO.VMODIFW, 
	c_VUELTO.ZADSFW 
from ZooLogic.COMPROBANTEV as c_VUELTO
where ( ( c_VUELTO.ANULADO = 0 ) and ( c_VUELTO.FVUELTO = 1 ) ) and
	( ( @ITEMVALORES_FECHA_DESDE is null ) OR ( c_VUELTO.FFCH >= @ITEMVALORES_FECHA_DESDE ) ) and 
	( ( @ITEMVALORES_FECHA_HASTA is null ) OR ( c_VUELTO.FFCH <= @ITEMVALORES_FECHA_HASTA ) )
)