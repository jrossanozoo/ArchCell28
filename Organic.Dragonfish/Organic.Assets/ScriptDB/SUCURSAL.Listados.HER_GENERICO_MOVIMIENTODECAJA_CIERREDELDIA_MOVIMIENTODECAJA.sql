	( ( @MOVIMIENTODECAJA_SECUENCIA_DESDE is null ) OR ( c_MOVIMIENTODECAJA.FactSec >= @MOVIMIENTODECAJA_SECUENCIA_DESDE ) ) and 
	( ( @MOVIMIENTODECAJA_SECUENCIA_HASTA is null ) OR ( c_MOVIMIENTODECAJA.FactSec <= @MOVIMIENTODECAJA_SECUENCIA_HASTA ) ) and 
