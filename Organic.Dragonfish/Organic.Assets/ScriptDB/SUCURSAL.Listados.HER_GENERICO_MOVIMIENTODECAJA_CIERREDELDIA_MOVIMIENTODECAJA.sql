	( ( @MOVIMIENTODECAJA_NUMEROCOMPROBANTE_DESDE is null ) OR ( c_MOVIMIENTODECAJA.NumComp >= @MOVIMIENTODECAJA_NUMEROCOMPROBANTE_DESDE ) ) and 
