left join ORGANIZACION.SECOD as C_SEGMENTACION on C_SEGMENTACION.SCOD = C_SUCURSAL.SEG and 0 = Funciones.empty( C_SUCURSAL.SEG )
left join ZooLogic.PROV as C_PROVEEDOR on C_PROVEEDOR.CLCOD = C_PAGO.FPERSON 
left join ZooLogic.PROV as C_PROVEEDOROP on C_PROVEEDOROP.CLCOD = C_COMPAGO.FPERSON
where ( 1=1 ) and 
	( ( @MOVIMIENTODECAJA_VALOR_DESDE is null ) OR ( c_MOVIMIENTODECAJA.idValor >= @MOVIMIENTODECAJA_VALOR_DESDE ) ) and 
	( ( @MOVIMIENTODECAJA_VALOR_HASTA is null ) OR ( c_MOVIMIENTODECAJA.idValor <= @MOVIMIENTODECAJA_VALOR_HASTA ) ) and 
	( ( @MOVIMIENTODECAJA_FECHACOMPROBANTE_DESDE is null ) OR ( c_MOVIMIENTODECAJA.FechaCompr >= @MOVIMIENTODECAJA_FECHACOMPROBANTE_DESDE ) ) and 
