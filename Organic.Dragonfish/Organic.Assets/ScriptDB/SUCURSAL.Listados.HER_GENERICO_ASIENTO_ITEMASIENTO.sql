	ORDER BY (CASE WHEN c_ITEMASIENTO.CtaDebe IS NULL THEN 1 ELSE 0 END), c_ITEMASIENTO.CtaDebe, (CASE WHEN c_ITEMASIENTO.CtaHaber IS NULL THEN 1 ELSE 0 END), c_ITEMASIENTO.CtaHaber 
