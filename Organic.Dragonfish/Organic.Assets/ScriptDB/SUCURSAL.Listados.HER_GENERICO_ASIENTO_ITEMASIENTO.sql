	( ( @ASIENTO_FECHAEXPO_HASTA is null ) OR ( C_ASIENTO_CABECERA.FECEXPO <= @ASIENTO_FECHAEXPO_HASTA ) ) and 
	( ( @ASIENTO_FECHAIMPO_DESDE is null ) OR ( C_ASIENTO_CABECERA.FECIMPO >= @ASIENTO_FECHAIMPO_DESDE ) ) and 
	( ( @ASIENTO_FECHAIMPO_HASTA is null ) OR ( C_ASIENTO_CABECERA.FECIMPO <= @ASIENTO_FECHAIMPO_HASTA ) ) and 
	( ( @ASIENTO_FECHAMODIFICACIONFW_DESDE is null ) OR ( C_ASIENTO_CABECERA.FMODIFW >= @ASIENTO_FECHAMODIFICACIONFW_DESDE ) ) and 
	( ( @ASIENTO_FECHAMODIFICACIONFW_HASTA is null ) OR ( C_ASIENTO_CABECERA.FMODIFW <= @ASIENTO_FECHAMODIFICACIONFW_HASTA ) ) and 
