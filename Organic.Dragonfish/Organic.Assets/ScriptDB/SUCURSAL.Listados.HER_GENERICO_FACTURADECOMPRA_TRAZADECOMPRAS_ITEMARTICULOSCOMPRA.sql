	C_ARTICULO.ARTDES as ARTICULO_ARTDES,
	C_COLOR.COLDES as COLOR_COLDES,
	C_TALLE.DESCRIP as TALLE_Descrip,
	case when funciones.empty( c_ITEMARTICULOSCOMPRA.CCOSTO ) = 1 then C_CENTRODECOSTO_CABECERA.COSDES else C_CENTRODECOSTO.COSDES end as CENTRODECOSTO_COSDES,
	C_ARTICULO.ARTCOD as ARTICULO_ARTCOD,
	case when funciones.empty( c_ITEMARTICULOSCOMPRA.CCOSTO ) = 1 then C_CENTRODECOSTO_CABECERA.COSCOD else C_CENTRODECOSTO.COSCOD end as CENTRODECOSTO_COSCOD,
	C_COLOR.COLCOD as COLOR_COLCOD,
	C_TALLE.CODIGO as TALLE_Codigo
 from [Listados].[Vista_TrazaItemsCDet] as c_TRAZA cross join Parametros
 inner join [Listados].[Vista_ComprobanteCDet] as c_ITEMARTICULOSCOMPRA on c_ITEMARTICULOSCOMPRA.CODIGO = c_TRAZA.CODIGO_ACTUAL and c_ITEMARTICULOSCOMPRA.IDITEM = c_TRAZA.IDITEM
 left join ZooLogic.ART as C_ARTICULO on C_ARTICULO.ARTCOD = C_ITEMARTICULOSCOMPRA.FART and 0 = Funciones.empty( C_ITEMARTICULOSCOMPRA.FART )
 left join ZooLogic.CCOSTO as C_CENTRODECOSTO on C_CENTRODECOSTO.COSCOD = C_ITEMARTICULOSCOMPRA.CCOSTO and 0 = Funciones.empty( C_ITEMARTICULOSCOMPRA.CCOSTO )
 left join ZooLogic.COL as C_COLOR on C_COLOR.COLCOD = C_ITEMARTICULOSCOMPRA.FCOLO and 0 = Funciones.empty( C_ITEMARTICULOSCOMPRA.FCOLO )
 left join ZooLogic.TALLE as C_TALLE on C_TALLE.CODIGO = C_ITEMARTICULOSCOMPRA.FTALL and 0 = Funciones.empty( C_ITEMARTICULOSCOMPRA.FTALL )
 inner join [Listados].[Vista_ComprobanteC] as C_FACTURADECOMPRA_CABECERA on C_FACTURADECOMPRA_CABECERA.CODIGO = c_TRAZA.CODIGO_ACTUAL and C_FACTURADECOMPRA_CABECERA.TIPO = c_TRAZA.TIPO
 left join ZooLogic.CCOSTO as C_CENTRODECOSTO_CABECERA on C_CENTRODECOSTO_CABECERA.COSCOD = C_FACTURADECOMPRA_CABECERA.CCOSTO and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.CCOSTO )
 left join ZooLogic.CONDPAGO as C_CONDICIONDEPAGO74_CABECERA on C_CONDICIONDEPAGO74_CABECERA.CLCOD = C_FACTURADECOMPRA_CABECERA.CONDPAGO and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.CONDPAGO )
 left join ZooLogic.DCCOSTO as C_DISTRIBUCIONCENTRODECOSTO_CABECERA on C_DISTRIBUCIONCENTRODECOSTO_CABECERA.CODIGO = C_FACTURADECOMPRA_CABECERA.DISCCOS and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.DISCCOS )
 left join ZooLogic.LPRECIO as C_LISTADEPRECIOS76_CABECERA on C_LISTADEPRECIOS76_CABECERA.LPR_NUMERO = C_FACTURADECOMPRA_CABECERA.CODLISTA and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.CODLISTA )
 left join ZooLogic.MONEDA as C_MONEDA_CABECERA on C_MONEDA_CABECERA.CODIGO = C_FACTURADECOMPRA_CABECERA.MONEDA and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.MONEDA )
 left join ZooLogic.PROV as C_PROVEEDOR_CABECERA on C_PROVEEDOR_CABECERA.CLCOD = C_FACTURADECOMPRA_CABECERA.FPERSON and 0 = Funciones.empty( C_FACTURADECOMPRA_CABECERA.FPERSON )
 left join ZooLogic.PROVINCI as C_PROVINCIA_CABECERA on C_PROVINCIA_CABECERA.PRV_COD = C_PROVEEDOR_CABECERA.CLPRV and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLPRV )
 left join ORGANIZACION.SITFISCAL as C_SITUACIONFISCAL_CABECERA on C_SITUACIONFISCAL_CABECERA.CODIGO = C_PROVEEDOR_CABECERA.CLIVA and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLIVA )
 left join ORGANIZACION.SUC as C_SUCURSAL_CABECERA on C_SUCURSAL_CABECERA.CODIGO = cast( funciones.obtenercodigodesucursaldeunabase(@basededatos) as varchar( 10 ) ) and 0 = Funciones.empty( cast( funciones.obtenercodigodesucursaldeunabase(@basededatos) as varchar( 10 ) ) )
 left join ORGANIZACION.TSUCUR as C_TIPOSUCURSAL_CABECERA on C_TIPOSUCURSAL_CABECERA.TCOD = C_SUCURSAL_CABECERA.TIPO and 0 = Funciones.empty( C_SUCURSAL_CABECERA.TIPO )
 left join ZooLogic.CLASPROV as C_CLASIFICACIONPROVEEDOR_CABECERA on C_CLASIFICACIONPROVEEDOR_CABECERA.CLACOD = C_PROVEEDOR_CABECERA.CLAS and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLAS )
 left join ZooLogic.CONDPAGO as C_CONDICIONDEPAGO52_CABECERA on C_CONDICIONDEPAGO52_CABECERA.CLCOD = C_PROVEEDOR_CABECERA.CLCONDPAG and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLCONDPAG )
 left join ZooLogic.SIPRIBDATADIC as C_DATOSADICIONALESSIPRIB_CABECERA on C_DATOSADICIONALESSIPRIB_CABECERA.CODIGO = C_PROVEEDOR_CABECERA.CLSIPRIB and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLSIPRIB )
 left join ZooLogic.IMPUESTO as C_IMPUESTO57_CABECERA on C_IMPUESTO57_CABECERA.CODIGO = C_PROVEEDOR_CABECERA.CLRETGAN and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLRETGAN )
 left join ZooLogic.IMPUESTO as C_IMPUESTO58_CABECERA on C_IMPUESTO58_CABECERA.CODIGO = C_PROVEEDOR_CABECERA.CLRETSUSS and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLRETSUSS )
 left join ZooLogic.IMPUESTO as C_IMPUESTO59_CABECERA on C_IMPUESTO59_CABECERA.CODIGO = C_PROVEEDOR_CABECERA.CLRETIVA and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLRETIVA )
 left join ORGANIZACION.LSUCUR as C_LINEASUCURSAL_CABECERA on C_LINEASUCURSAL_CABECERA.LSCOD = C_SUCURSAL_CABECERA.LINEA and 0 = Funciones.empty( C_SUCURSAL_CABECERA.LINEA )
 left join ZooLogic.LPRECIO as C_LISTADEPRECIOS55_CABECERA on C_LISTADEPRECIOS55_CABECERA.LPR_NUMERO = C_PROVEEDOR_CABECERA.CLLISTPRE and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.CLLISTPRE )
 left join ZooLogic.PAISES as C_PAISES_CABECERA on C_PAISES_CABECERA.PCOD = C_PROVEEDOR_CABECERA.PPAIS and 0 = Funciones.empty( C_PROVEEDOR_CABECERA.PPAIS )
 left join ORGANIZACION.SECOD as C_SEGMENTACION_CABECERA on C_SEGMENTACION_CABECERA.SCOD = C_SUCURSAL_CABECERA.SEG and 0 = Funciones.empty( C_SUCURSAL_CABECERA.SEG )
 where ( 1=1 ) and 
	-- Cuando el tope de los niveles superiores es 0 no tiene sentido mostrar las cancelaciones en la base puesto que estas afectan
	-- a un comprobante de nivel superior que no se estaría mostrando y no tiene relación directa con el comprobante base enonces
	-- excluimos las cancelaciones a nivel 0 (base)...
	( case when coalesce( Parametros.TopeSuperior, -99 ) = 0 then power( 0, c_TRAZA.CANCELACION_A_NIVEL0 ) else 1 end = 1 ) and
	( c_TRAZA.NIVEL between coalesce( Parametros.TopeSuperior, -99) and coalesce( Parametros.TopeInferior, 99 ) - case when c_TRAZA.TIPO_BASE = 'Remito' and coalesce( Parametros.EstaFiltrandoPedidosYRemitos, 0 ) = 1 and coalesce( Parametros.TopeInferior, 99 ) > 0 then 1 else 0 end ) and
	( ( @FACTURADECOMPRA_FECHA_DESDE is null ) OR ( c_TRAZA.FECHA_COMPROBANTE_BASE >= @FACTURADECOMPRA_FECHA_DESDE ) ) and 
	( ( @FACTURADECOMPRA_FECHA_HASTA is null ) OR ( c_TRAZA.FECHA_COMPROBANTE_BASE <= @FACTURADECOMPRA_FECHA_HASTA ) )
 /* Recomendado order by c_TRAZA.CODIGO_RAIZ, c_TRAZA.ORDEN */
